package ex2_8;

public class CompOperstor {
	public static void main(String[] args) {
		System.out.print((3 == 4) + " ");
		System.out.print((3 != 4) + " ");
		System.out.print((3 > 4) + " ");
		System.out.print((4 > 3) + " ");

		System.out.print((3 == 3 && 4 == 7) + " ");
		System.out.print((3 == 3 || 4 == 7) + " ");
	
	}
}
